    public String getTotal(){
        Double Tot=Double.parseDouble(txtNot1.getText())+Double.parseDouble(txtNot2.getText());
        return String.valueOf(Tot);
    }
    public String getObs(){
        Double Tot=Double.parseDouble(txtNot1.getText())+Double.parseDouble(txtNot2.getText());
        if(Tot<9){
            return "F";
        }else if(Tot>=9 && Tot<14){
            return "S";
        }else {
            return "E";
        }
    }
    public void print(String IdMat){
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            tabNot.setModel(modelo);
            PreparedStatement ps = null, ps1 = null;
            ResultSet rs = null, rs1 = null;
            Connection conn = null;
            conn = Conector.getConector();
            String sql = "SELECT Estudiantes.IdEstudiante,NomEstudiante,ApeEstudiante,PrimerBimestre,SegundoBimestre,Total,Obs FROM Estudiantes,calificacion where Estudiantes.IdEstudiante=calificacion.IdEstudiante and IdMateria= " + IdMat;
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int num = rsmd.getColumnCount();
            modelo.addColumn("Id");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellido");
            modelo.addColumn("Nota1");
            modelo.addColumn("Nota2");
            modelo.addColumn("Total");
            modelo.addColumn("Obs");
            while (rs.next()) {
                Object[] filas = new Object[num];
                for (int i = 0; i < num; i++) {
                    filas[i] = rs.getObject(i + 1);
                }

                modelo.addRow(filas);

            }
            //informacion del profesor
            ps1 = conn.prepareStatement("Select * from profesor,materias where profesor.IdProfesor=materias.IdProfesor and IdMateria=?");
            ps1.setString(1, IdMat);
            rs1 = ps1.executeQuery();
            if (rs1.next()) {
                txtNom.setText(rs1.getString("NomProfesor") + " " + rs1.getString("ApeProfesor"));
                txtMateria.setText(rs1.getString("Materia"));
                txtIDprof.setText(rs1.getString("Profesor.IdProfesor"));
                txtEmail.setText(rs1.getString("emailProfesor"));
                txttlf.setText(rs1.getString("TelfProfesor"));
                conn.close();
            }
        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }
